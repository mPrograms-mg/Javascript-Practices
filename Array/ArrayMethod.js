// Array Method
let arr = [1,2,3,4,5];


// Add element at last index
arr.push("Mahesh");
console.log(arr);

// Remove element at last index
arr.pop();
console.log(arr); 

//Add element in starting index
arr.unshift(true,2.55,"Mahesh")
console.log(arr);

// remove element in starting index
arr.shift();
console.log(arr);
